import java.lang.*;

class arith
{
	public static void main(String ar[])
	{
		int a,b,c;
		a=6;
		b=3;

		c=a+b;
		System.out.println("Sum = "+c);

		c=a-b;
		System.out.println("Difference = "+c);

		c=a*b;
		System.out.println("Product = "+c);

		c=a/b;
		System.out.println("Quotient = "+c);
	}
};